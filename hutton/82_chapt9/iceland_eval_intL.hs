-- interleave

intL :: a -> [a] -> [[a]]
intL x []     = [[x]]
intL x (y:ys) = (x:y:ys) : map (y:) (intL x ys)

> intL 5 [1,2,3]

= (5:1:[2,3])

=

[5,1,2,3] : map (1:) (intL 5 [2,3])
=
[5,1,2,3] : map (1:) ([5,2,3] : map (2:) (intL 5 [3]))
= 
[5,1,2,3] : map (1:) ([5,2,3] : map (2:) ([5,3] : map (3:) (intL 5 [])))
=
[5,1,2,3] : map (1:) ([5,2,3] : map (2:) ([5,3] : map (3:) [[5]]))
=
[5,1,2,3] : map (1:) ([5,2,3] : map (2:) ([5,3] : [[3,5]]))
=
[5,1,2,3] : map (1:) ([5,2,3] : map (2:) [[5,3], [3,5]])
=
[5,1,2,3] : map (1:) ([5,2,3] : [[2,5,3], [2,3,5]])
=
[5,1,2,3] : map (1:) [[5,2,3], [2,5,3], [2,3,5]]
=
[5,1,2,3] : [[1,5,2,3], [1,2,5,3], [1,2,3,5]]
=
[[5,1,2,3], [1,5,2,3], [1,2,5,3], [1,2,3,5]]


