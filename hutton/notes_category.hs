One of the requirements of a category is that each object of the category has an identity morphism whose source and target are that object

taking the objects of our category to be types and morphisms to be functions between them, id corresponds to the identity morphisms

you can view id as being a family of monomorphic identity functions rather than a single polymorphic function

each of these monomorphic instantiations is an identity morphism for a particular type

A catamorphism is the unique F-algebra morphism from the initial F-algebra to another F-algebra

Id is an endomorphism, meaning that the source and target of the morphism are the same.
